if (isset($_GET['error'])) {
    switch ($_GET['error']) {
        case 'invalid':
            $error = "Email ou senha incorretos.";
            break;
        case 'test_completed':
            $error = "Este teste já foi concluído.";
            break;
        default:
            $error = "Erro desconhecido.";
    }
}
